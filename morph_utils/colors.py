#MOUSE 

def get_mouse_color_dict(path):
    color_df = pd.read_csv(path)
    color_df['key'] = color_df['id'].astype(str) + ' ' + color_df['label']
    color_keys = color_df['key']
    color_values = color_df['color']
    color_dict = dict(zip(color_keys,color_values))
    return color_dict

